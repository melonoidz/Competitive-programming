    n++;
    int ans = 0;
    // 1の位：n/10, n%10
    // 10の位：n/100, n%100
    // 100の位：n/1000, n%1000
    for (int i = 1; i <= 1000000000; i *= 10) {
        ans += n / (i * 10) * i;
        auto p = n % (i * 10);
        //i個1が連なる or 
        //i=10, p=12 たしかに 
        ans += max(0LL, min(i, p - i));
    }
    cout << ans << endl;
